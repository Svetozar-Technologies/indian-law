#!/usr/bin/env node
import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { parseRegionalSourceRows } from "./lib/html.mjs";
import { fetchText, sleep } from "./lib/http.mjs";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const output = path.resolve(ROOT, args.output ?? "data/regional-sources.discovered.json");
  const languagePath = path.resolve(ROOT, args.languages ?? "data/languages.json");
  const maxPages = Number(args["max-pages"] ?? 1);
  const delayMs = Number(args["delay-ms"] ?? 1100);
  const config = JSON.parse(await readFile(languagePath, "utf8"));
  const sources = [];
  const errors = [];

  for (const language of config.languages.filter((entry) => entry.regionalSlug)) {
    for (let page = 0; page < maxPages; page += 1) {
      const url = new URL(language.officialSource);
      if (page > 0) {
        url.searchParams.set("page", String(page));
      }
      try {
        const html = await fetchText(url.toString(), { retries: 1, timeoutMs: 20000 });
        const rows = parseRegionalSourceRows(html, language.code, language.officialSource);
        sources.push(...rows);
        if (rows.length === 0) {
          break;
        }
      } catch (error) {
        errors.push({
          language: language.code,
          url: url.toString(),
          message: error.message
        });
        break;
      }
      await sleep(delayMs);
    }
  }

  const uniqueSources = [];
  for (const source of sources) {
    if (!uniqueSources.some((entry) => entry.language === source.language && entry.url === source.url)) {
      uniqueSources.push(source);
    }
  }

  const payload = {
    lastVerified: new Date().toISOString().slice(0, 10),
    sources: uniqueSources,
    errors,
    notes: [
      "Generated by scripts/discover-regional-sources.mjs from Legislative Department regional-language pages.",
      "These are official source files. Converting scanned PDFs to Markdown may require an OCR step outside the deterministic PR checks."
    ]
  };

  await writeFile(output, `${JSON.stringify(payload, null, 2)}\n`);
  console.log(`Discovered ${uniqueSources.length} regional sources -> ${path.relative(ROOT, output)}`);
}

function parseArgs(argv) {
  const args = {};
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (!arg.startsWith("--")) {
      continue;
    }
    const [key, inlineValue] = arg.slice(2).split("=");
    if (inlineValue !== undefined) {
      args[key] = inlineValue;
    } else if (argv[index + 1] && !argv[index + 1].startsWith("--")) {
      args[key] = argv[index + 1];
      index += 1;
    } else {
      args[key] = true;
    }
  }
  return args;
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
